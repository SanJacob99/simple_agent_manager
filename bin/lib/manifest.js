/**
 * sam.json read / write / synthesize, plus a JS-side shape validator.
 *
 * The schema source of truth is `shared/user-tool-manifest.ts` (TypeBox).
 * The CLI is plain JS and cannot load TypeBox-from-TypeScript without a
 * loader, so this file mirrors the required-fields contract. Keep them
 * in sync.
 */

import fs from 'fs';
import path from 'path';

export const MANIFEST_FILENAME = 'sam.json';

export function manifestPath(toolDir) {
  return path.join(toolDir, MANIFEST_FILENAME);
}

/**
 * Read sam.json from `toolDir`. Returns null if absent. Throws with a
 * caller-friendly message on parse / shape failures.
 */
export function readManifest(toolDir) {
  const p = manifestPath(toolDir);
  if (!fs.existsSync(p)) return null;
  let raw;
  try {
    raw = fs.readFileSync(p, 'utf8');
  } catch (err) {
    throw new Error(`Cannot read ${p}: ${err.message}`);
  }
  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (err) {
    throw new Error(`Invalid JSON in ${p}: ${err.message}`);
  }
  validateManifestShape(parsed, p);
  return parsed;
}

export function writeManifest(toolDir, manifest) {
  validateManifestShape(manifest, manifestPath(toolDir));
  fs.writeFileSync(
    manifestPath(toolDir),
    JSON.stringify(manifest, null, 2) + '\n',
    'utf8',
  );
}

/**
 * Build a v0 manifest for a freshly installed tool that didn't ship its
 * own sam.json. Marks itself as enabled and includes a `_todo` field
 * so the operator notices the synthesized values when they open it.
 */
export function synthesizeManifest({ name, source }) {
  return {
    name,
    version: '0.0.0',
    source,
    disabled: false,
    _todo:
      'Auto-generated by `sam install`. Fill in real version, description, ' +
      'author, license. See docs/concepts/user-tools-guide.md.',
  };
}

function validateManifestShape(m, where) {
  if (!m || typeof m !== 'object' || Array.isArray(m)) {
    throw new Error(`${where}: expected a JSON object`);
  }
  const required = ['name', 'version', 'source', 'disabled'];
  for (const k of required) {
    if (!(k in m)) throw new Error(`${where}: missing required field "${k}"`);
  }
  for (const k of ['name', 'version', 'source']) {
    if (typeof m[k] !== 'string' || m[k].length === 0) {
      throw new Error(`${where}: "${k}" must be a non-empty string`);
    }
  }
  if (typeof m.disabled !== 'boolean') {
    throw new Error(`${where}: "disabled" must be a boolean`);
  }
}
